package peter;

/**
 * 给你一个字符串 s ，将该字符串中的大写字母转换成相同的小写字母，返回新的字符串。
 * <p>
 * ?
 * <p>
 * 示例 1：
 * <p>
 * 输入：s = "Hello"
 * 输出："hello"
 * <p>
 * <p>
 * 示例 2：
 * <p>
 * 输入：s = "here"
 * 输出："here"
 * <p>
 * <p>
 * 示例 3：
 * <p>
 * 输入：s = "LOVELY"
 * 输出："lovely"
 * <p>
 * <p>
 * ?
 * <p>
 * 提示：
 * <p>
 * <p>
 * 1 <= s.length <= 100
 * s 由 ASCII 字符集中的可打印字符组成
 * <p>
 * https://leetcode-cn.com/problems/to-lower-case
 */
public class _709_To_Lower_Case {
    public String toLowerCase(String s) {
        return s.toLowerCase();
    }
}